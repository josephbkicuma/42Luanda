#include "../includes/ft.h"

void    ft_rev_int_tab(int *tab, int size)
{
    int n;

    n = 0;
    size--;
    while (n <= (size / 2))
    {
        ft_swap(&tab[n], &tab[size - n]);
        n++;
    }
}
